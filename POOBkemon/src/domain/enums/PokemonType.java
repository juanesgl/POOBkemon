package domain.enums;

/**
 * Enum representing the different elemental types of Pokemon and moves.
 * These types determine type effectiveness in battle and influence damage calculations.
 */
public enum PokemonType {
    /**
     * Normal type Pokemon and moves.
     */
    NORMAL,

    /**
     * Fire type Pokemon and moves.
     */
    FIRE,

    /**
     * Water type Pokemon and moves.
     */
    WATER,

    /**
     * Electric type Pokemon and moves.
     */
    ELECTRIC,

    /**
     * Grass type Pokemon and moves.
     */
    GRASS,

    /**
     * Ice type Pokemon and moves.
     */
    ICE,

    /**
     * Fighting type Pokemon and moves.
     */
    FIGHTING,

    /**
     * Poison type Pokemon and moves.
     */
    POISON,

    /**
     * Ground type Pokemon and moves.
     */
    GROUND,

    /**
     * Flying type Pokemon and moves.
     */
    FLYING,

    /**
     * Psychic type Pokemon and moves.
     */
    PSYCHIC,

    /**
     * Bug type Pokemon and moves.
     */
    BUG,

    /**
     * Rock type Pokemon and moves.
     */
    ROCK,

    /**
     * Ghost type Pokemon and moves.
     */
    GHOST,

    /**
     * Dragon type Pokemon and moves.
     */
    DRAGON,

    /**
     * Dark type Pokemon and moves.
     */
    DARK,

    /**
     * Steel type Pokemon and moves.
     */
    STEEL,

    /**
     * Fairy type Pokemon and moves.
     */
    FAIRY;

}